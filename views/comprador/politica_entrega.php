                <h4>Formas y tiempos de entrega</h4>
                <h6>Envío estándar</h6>

                El tiempo de entrega por mensajería varía dependiendo del destino y cuenta con un plazo de entrega de 1
                a 4 días hábiles. Todos los envíos se realizan una vez que se haya recibido el importe total de la
                venta.<br>

                ***** No contamos con piso de exhibición. Por razones de seguridad, no se permiten recolecciones de mercancía. Gracias por tu comprensión. *****<br>

                <h4>Seguro del Envío</h4>
                Aseguramos todos los envíos con nuestros socios de logística para brindarle máxima seguridad en tus compras en Mi tienda Unir MX S.A de C.V.

                El seguro se contrata por Mi tienda Unir MX S.A de C.V y no tiene ningún costo adicional para el cliente.<br>
                El monto cubre 100% del valor de la mercancía – sin deducibles, sin límites.<br>
                El seguro cubre daños, pérdida o robo.<br>
                Todos nuestros envíos están asegurados.<br>

                Por supuesto prevenimos daños en el envío. Manejamos productos sensibles y sabemos empacarlos para máxima protección.<br>

                <strong>***** Aviso importante *****</strong>

                Para mantener la validez del seguro, te recordamos siempre verificar los siguientes puntos al recibir su mercancía:

                <ul style="list-style: circle">
                    <li>Inspección visual para corroborar que el empaque viene en óptimas condiciones.</li>
                    <li>Verificar que las cintas y sellos no hayan sido violados.</li>
                    <li>Al firmar de recibido, verificar que firmas únicamente por las guías que te han sido entregadas.</li>
